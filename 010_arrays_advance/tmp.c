#include <stdio.h>

double calc_avg_temp(const double tmp_arr[], size_t len)
{
    double sum = 0;
    int count = 0;
    for (size_t i = 0; i < len; i++)
    {
        if (tmp_arr[i] == -1000)
        {
            continue;
        }
        sum += tmp_arr[i];
        count++;
    }
    double total = sum / count;
    return total;
}

